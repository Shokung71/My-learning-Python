#ตัวแปร list เสื้อในตู้
cloth_list = ["1.เสื้อชมพู","2.เสื้อฟ้า","3.เสื้อเขียว","4.เสื้อลายเป็ด","5.เสื้อลายหมี","6.เสื้อลายหัวใจ","7.เสื้อลายเมฆ","8.เสื้อลายโปเกมอน","9.เสื้อลายหมีพูห์","10.เสื้อลายสูตรคณิตศาสตร์"]

#########################ส่วนฟังก์ชัน##############################

def edit_list(cloth_list):    #ฟังก์ชันแก้ไขข้อมูล
  print("\nกำลังเริ่มทำการแก้ไขข้อมูล")
  print(cloth_list)
  no_cloth = int(input("เสื้อตัวไหนไม่ถูกต้อง ตอบเป็นตัวเลข :"))          #ถามตำแหน่งที่ list ไม่ถูกต้อง
  cloth_list[no_cloth-1] = input("เสื้อผ้าใหม่ คือ(ใส่ลำดับที่เพิ่มเติมด้วย) ")   
#ได้ชื่อเสื้อใหม่ตาม list ที่ได้เลือกไว้ , no_cloth-1 เพราะคำตอบข้างบน จะไม่ตรงกับตำแหน่งที่เราเลือก(เช่น พิมพ์ 2 ไปแก้ลำดับ 3)
  print("\nนี่คือผลการแก้ไขข้อมูล")
  print(cloth_list)


def data_t_or_f():          #ฟังก์ชันเช็คข้อมูล
  answer = int(input("ข้อมูลถูกต้องหรือไม่? ใช่ ตอบ 1, ไม่ใช่ ตอบ 2 :"))
  while answer !=1:
    if answer == 2:
      edit_list(cloth_list)
      answer = int(input("ข้อมูลถูกต้องหรือไม่? ใช่ ตอบ 1, ไม่ใช่ ตอบ 2 :"))
    else:
      print("กรุณาตอบใหม่อีกครั้ง")
    
  print(cloth_list)
    

def end_tasks():            #ใช้งานโปรแกรมเสร็จสิ้น
  print("\nOK")
